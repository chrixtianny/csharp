using System;

namespace CSharpExamples
{
  class TiposDeDados
  {
    static void Main(string[] args)
    {
      int meuInteiro = 5;               // inteiro 
      double meuDouble = 5.99D;  // ponto flutuante
      char minhaLetra = 'A';         // caractere
      bool meuBool = true;          // booleano
      string meuTexto = "Ola";     // string

      Console.WriteLine(meuInteiro);
      Console.WriteLine(meuDouble);
      Console.WriteLine(minhaLetra);
      Console.WriteLine(meuBool);
      Console.WriteLine(meuTexto);
    }
  }
}